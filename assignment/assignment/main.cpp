// 赋值运算符

#include <iostream>
using namespace std;
int main(int argc, const char * argv[]) {
    int num1 = 1;
    num1+=2;
    cout << num1 << endl;
    
    num1-=2;
    cout << num1 << endl;
   
    num1*=2;
    cout << num1 << endl;
    
    num1/=2;
    cout << num1 << endl;

    
    num1%=2;
    cout << num1 << endl;
    
    
}
